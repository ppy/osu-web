    /**
     * Store a newly created resource in storage.
     *
     * @return Response
     */
    public function store()
    {
        $depth = 0;
        $search = Input::get('parent');
        while (true) {
            $parent = Post::find($search);
            if (!$parent) {
                break;
            }
            $depth++;

            $search = $parent->parent_id;
        }

        $post = new Post();
        $post->user()->associate(Auth::user());
        $post->parent_id = Input::get('parent');
        $post->depth = $depth;
        $post->text = Input::get('text');
        $post->save();
    }

